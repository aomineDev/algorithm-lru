    private void setTableModel() {
        Object[] columns = {" "};

        DefaultTableModel model = new DefaultTableModel(null, columns);

        table.setModel(model);
        table.getTableHeader().setResizingAllowed(false);
        table.getTableHeader().setReorderingAllowed(false);
        table.setRowHeight(30);

        for (int i = 1; i <= marcos; i++) {
            model.addRow(new Object[]{"marco " + i});
        }
    }

    private void setTableHeader() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setColumnCount(0);

        model.addColumn(" ");
        for (Integer pagina : lru.getPaginasParaAcceder()) {
            model.addColumn(pagina);
        }
    }

    private void setTableData() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        model.setRowCount(0);

        int contador = 1;
        for (Integer[] filaMemoria : lru.getMemoria()) {
            Object[] fila = new Object[filaMemoria.length + 1];
            fila[0] = "Marco " + contador;
            for (int i = 1; i < fila.length; i++) {
                fila[i] = filaMemoria[i - 1];
            }
            model.addRow(fila);
            contador++;
        }
    }

