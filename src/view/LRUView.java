    private void btnIngresarPaginasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarPaginasActionPerformed
        columna = 0;
        String respuesta = JOptionPane.showInputDialog(rootPane, "Ingrese las paginas");

        try {
            String[] paginasStr = respuesta.split(" ");
            Integer[] paginas = new Integer[paginasStr.length];

            for (int i = 0; i < paginas.length; i++) {
                paginas[i] = Integer.valueOf(paginasStr[i]);
            }

            lru = new LRU(marcos, paginasProceso);
            lru.setPaginasParaUsar(paginas);
            setTableHeader();
            setTableData();
            btnSiguiente.setEnabled(true);
            tituloResultados.setText("Datos");
            resultados.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Las pagias ingresadas no son validos", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NullPointerException e) {
            System.err.println("Cerro el dialog");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarPaginasActionPerformed

    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        lru.accesoAPagina(columna);
        setTableData();

        resultados.setText(lru.getDatos());

        columna++;

        if (columna == lru.getCantidadDePaginasAAcceder()) {
            btnSiguiente.setEnabled(false);
            tituloResultados.setText("Resultados");
            resultados.setText(lru.toString());
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

